    <div class="container">
        <p><span class="bold">The Township of Derry was incorporated August 1, 1729, when Lancaster County divided the territory for tax purposes. At that time, the boundaries of Derry Township were much larger and included what are currently three other townships within Lancaster and Dauphin County. The western portion became Derry Township and the eastern portion became Londonderry Township. The first official government in Derry Township was established in 1759. Five officials were elected for one-year terms. The number of elected officials in Derry remains five, however the terms are six years.
</span></p>

<p>Derry became a part of Dauphin County when it was established in 1785. In 1787, the population of Derry Township was 198. The most recent census records the population as 21,273.</p>

<span>1700s</span>
<p>Scott-Irish, Germans, and Swiss-Germans migrated to Derry Township for religious freedom. The Township was primarily a rural farming community. The Derry Presbyterians built a log church and the small community around it became known as Derry Church.</p>

<span>1800s</span>
<p>The Horseshoe and Reading turnpikes connecting Derry Township to the rest of Pennsylvania were constructed, better known today as S.R. 322 and S.R. 422. The Union Canal was completed in 1827 bringing together the Schuylkill and Susquehanna rivers. Items such as flour, food, livestock, coal and lumber were shipped on the canal. The Lebanon Valley Railroad came to Derry Township in 1858. The rails serviced the local quarries, flour and saw mills, distilleries, tanneries, and lime kilns. In 1860 the local population was 2,300.</p>

<p>Public education began in Derry Township with the Pennsylvania Free School Act of 1834. The Township's first school was the Derry Church One-room School, built in 1844 and located on Mansion Road. Over the next few decades, twelve more one-room schools were constructed in the Township.
</p>
<p>Hungarian and Italian immigrants, plus migrant African-Americans from Virginia came to Derry Township for economic opportunities, finding employment in the local quarries. Limestone and brownstone were shipped for building material. While most of them were transient workers, many Italian stonecutters brought their families and settled in the area.</p>   
<span>1900s</span>

<p>Milton S. Hershey returned to his birthplace to build a new factory for the Hershey Chocolate Company in 1903. The Hershey Volunteer Fire Company was organized two years later. The area boomed with construction for the next 15 years with the development of a model industrial town. New structures included sites such as the Cocoa House (1905), Hersheypark (1907), Hershey Laundry (1908), Ballroom (1909), Hershey Department Store (1910), Hershey Park Pool (1912), Hershey Trust Company (1914), the M.S. Hershey Consolidated School (1914), and the Press Building (1916). Hershey Industrial School for disadvantaged orphaned boys was established 1909, today known as Milton Hershey School.</p>

<p>During the Great Depression of the 1930s the Community Building, Hotel Hershey, Milton Hershey School Senior Hall, Hershey Sports Arena, Hershey Chocolate Corporation Windowless Office Building, and the Stadium were built.</p>

<span>20th Century</span>

<p>The Township of Derry, Hershey, remains a community with a small-town feel, with residents that place a high priority on family. The area offers a School District that excels academically, a full-service accredited police department, a state-of-the-art library, a recreation center with two swimming pools, a fitness center, and eight public parks. Hershey is conveniently located within 100 miles of diverse cultural activities in Harrisburg, Lancaster, Philadelphia, and New York.
</p>

